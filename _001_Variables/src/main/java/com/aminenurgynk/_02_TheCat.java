package com.aminenurgynk;


public class _02_TheCat {

    public static void main(String[] args) {

        int age = 4;
        String kind = "British Shorthair";
        String name = "Balim";
        String color = "Gray";

        System.out.println("Her age  : " + age);
        System.out.println("Kind of : " + kind);
        System.out.println("Her name   : " + name);
        System.out.println("Her color  : " + color);

        System.out.println("------------------------");

        age = 3;
        kind = "Street Cat";
        name = "Soda";
        color = "Brown";

        System.out.println("His age : " + age + " , Kind of : " + kind);
        System.out.println("His name: " + name  + " , His color : " + color);
    }

}
